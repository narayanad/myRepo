package SapleProject.Sample;

public class Example {

	public static void main(String[] args) 
	{
		int rc=5,starcount=5;
	    for (int i=1;i<=rc;i++) 
	    {
	      System.out.println();
	      for (int j=1;j<=starcount;j++)
	  
	      {
	    	  System.out.print("*");
	      }
	      
	      starcount--;
	    }
	}
}
